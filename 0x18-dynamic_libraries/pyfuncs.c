#include "lists.h"
/**
 * add - add 2 numbers .
 * @n1: number 1
 * @n2: number 2
 *
 * Return: n1 + n2
 */
int add(int n1, int n2)
{
	return (n1 + n2);
}
/**
 * sub - sub of 2 numbers .
 * @n1: number 1
 * @n2: number 2
 *
 * Return: n1 - n2
 */
int sub(int n1, int n2)
{
	return (n1 - n2);
}
/**
 * mul -  multiplies two integers
 * @a: number 1
 * @b: number 2
 *
 * Return: a * b
 */

int mul(int a, int b)
{
	return (a * b);
}
/**
 * div -  division of two integers
 * @a: number 1
 * @b: number 2
 *
 * Return: a / b
 */

int div(int a, int b)
{
	return (a / b);
}
/**
 * mod -  modulo of  two integers
 * @a: number 1
 * @b: number 2
 *
 * Return: a % b
 */

int mod(int a, int b)
{
	return (a % b);
}
